import time

logfile = 'log_file.log'


def log(msg):
    global logfile

    fh = open(logfile, 'a')
    fh.write(f'{time.asctime()} {msg}\n')
    fh.close()


def set_l(log_file):
    global logfile

    logfile = log_file
